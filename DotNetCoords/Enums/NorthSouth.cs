namespace DotNetCoords
{
    /// <summary>
    ///     Enumerated type defining whether a latitude is North or South of the equator
    /// </summary>
    public enum NorthSouth
    {
        /// <summary>
        ///     Latitude is north of the equator.
        /// </summary>
        North = 1,

        /// <summary>
        ///     Latitude is south of the equator.
        /// </summary>
        South = -1
    }
}